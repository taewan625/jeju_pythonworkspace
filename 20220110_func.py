'''
# 입력된 값의 평균 구하기
1. 함수호출 ()
avr(3,4,6,7,54,4) # 결과 --> 평균 : ?
단, 여러개의 매개변수를 받기 위해 *을 사용

함수정의

def avr(*v) :
    합구하기
    길이구하기 -> len(v)
'''
def avr(*args) :
    tot = 0         # 들어올 값을 초기화 해줌
    for i in args:
        tot = tot + i
    return tot / len(args)
n1 = avr(3,4,6,7,54,4)
print(n1)
# 해석: return tot 값은 숫자 데이터에 for 함수를 다 적용한 값이므로 거기에 원하는 수식'len(args)' 넣어서 평균 구함