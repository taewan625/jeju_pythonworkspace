# 여러개의 매개변수를 이용하는 함수 --> *매개변수

def addMany(*args) :                # 정의해야함
    #  숫자 -> 합
    tot = 0 # 초기화
    # 반복
    for i in args: # args[0] -> i
        tot = tot + i # 합을 구하기 위해 누적
    return tot         # 결과를 for 문으로 돌려줘야함               


